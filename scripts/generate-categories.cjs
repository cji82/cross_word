function applyFactualClues(entries) {
  for (const entry of entries) {
    const clue = FACTUAL_CLUES[entry.word];
    if (clue && isValidClue(clue, entry.word)) {
      entry.clue = clue;
    }
  }
}

function applyWordReplacements(entries) {
  for (const entry of entries) {
    const next = WORD_REPLACEMENTS[entry.word];
    if (next) entry.word = next;
  }
}

function prepareData() {
  for (const [key, category] of Object.entries(DATA)) {
    applyWordReplacements(category.words);
    applyWordReplacements(EXTRA_WORDS[key] || []);
    applyWordReplacements(WORD_PACKS[key] || []);
    // 사전 자동 동기화 단서는 동음이의어/인코딩 이슈가 있어 기본 단서를 사용한다.
    const used = new Set();
    const words = [];

    for (const entry of category.words) {
      if (countSyllables(entry.word) < MIN_SYLLABLES) continue;
      if (used.has(entry.word)) continue;
      if (isTruncatedClue(entry.clue)) continue;
      used.add(entry.word);
      words.push(entry);
    }

    for (const entry of EXTRA_WORDS[key] || []) {
      if (words.length >= EXPECTED) break;
      if (countSyllables(entry.word) < MIN_SYLLABLES) continue;
      if (used.has(entry.word)) continue;
      if (isTruncatedClue(entry.clue)) continue;
      used.add(entry.word);
      words.push(entry);
    }

    for (const entry of WORD_PACKS[key] || []) {
      if (words.length >= EXPECTED) break;
      if (countSyllables(entry.word) < MIN_SYLLABLES) continue;
      if (used.has(entry.word)) continue;
      if (isTruncatedClue(entry.clue)) continue;
      used.add(entry.word);
      words.push(entry);
    }

    if (words.length < EXPECTED) {
      throw new Error(`${key}: 2음절 이상 단어가 ${words.length}개뿐입니다 (${EXPECTED}개 필요)`);
    }

    category.words = words.slice(0, EXPECTED);
  }
}

function validate() {
  const errors = [];
  for (const [key, category] of Object.entries(DATA)) {
    const words = category.words.map((e) => e.word);
    const unique = new Set(words);
    if (words.length !== EXPECTED) {
      errors.push(`${key}: ${words.length}개 (기대 ${EXPECTED}개)`);
    }
    if (unique.size !== words.length) {
      const dupes = words.filter((w, i) => words.indexOf(w) !== i);
      errors.push(`${key}: 중복 단어 ${[...new Set(dupes)].join(', ')}`);
    }
    for (const { word, clue } of category.words) {
      const len = clue.length;
      if (len < 10 || len > 36) {
        errors.push(`${key}/${word}: clue 길이 ${len} (10-36 필요)`);
      }
      if (countSyllables(word) < MIN_SYLLABLES) {
        errors.push(`${key}/${word}: ${countSyllables(word)}음절 (최소 ${MIN_SYLLABLES}음절)`);
      }
      if (clue.includes(word)) {
        errors.push(`${key}/${word}: 단서에 정답 단어 포함`);
      }
      if (isTruncatedClue(clue)) {
        errors.push(`${key}/${word}: 단서 문장이 잘림`);
      }
      if (/\uFFFD/.test(clue)) {
        errors.push(`${key}/${word}: 단서 글자가 깨짐`);
      }
    }
  }
  if (errors.length) {
    console.error('검증 실패:\n' + errors.join('\n'));
    process.exit(1);
  }
}

function writeOutput() {
  const payload = encodeCategories(DATA);
  const content = [
    '/** crossword category data (encoded) */',
    `const __CW='${payload}';`,
    '',
  ].join('\n');
  fs.writeFileSync(OUT_PATH, content, 'utf8');
}

